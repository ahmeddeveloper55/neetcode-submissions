class Solution:
    def reverseString(self, s: List[str]) -> None:
        """
        Do not return anything, modify s in-place instead.
        """
        """
         we need to reverse string in plance :
         we need to take list with index 0 ,1,2,end-1
         if we follow two pointer approach 
         we will have 
         start , end 
         or there is easy reverse function will try it first
        """
        return s.reverse()
        